#!/usr/bin/env pwsh

# JIFAS KB Loader - FAST Execution

Write-Host ""
Write-Host "?????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "?  JIFAS Knowledge Base Loader - FAST Bulk Insert             ?" -ForegroundColor Cyan
Write-Host "?  Expected: 54 files loaded in < 1 minute                    ?" -ForegroundColor Cyan
Write-Host "?????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

Write-Host "Prerequisite Check..." -ForegroundColor Yellow
if (-not (Test-Path "KBLoader")) { Write-Host "? KBLoader not found"; exit 1 }
Write-Host "? Project structure OK" -ForegroundColor Green

cd KBLoader
Write-Host ""
Write-Host "Building..." -ForegroundColor Yellow
dotnet build --configuration Release -q
if ($LASTEXITCODE -ne 0) { Write-Host "? Build failed"; exit 1 }
Write-Host "? Build OK" -ForegroundColor Green

Write-Host ""
Write-Host "Running KB Loader (FAST MODE)..." -ForegroundColor Yellow
Write-Host ""

$start = Get-Date
dotnet run --configuration Release -- --yes
$elapsed = (Get-Date) - $start

Write-Host ""
Write-Host "?????????????????????????????????????????????????????????????????" -ForegroundColor Green
Write-Host "?  ? COMPLETE!  Time: $($elapsed.TotalSeconds) seconds                   ?" -ForegroundColor Green
Write-Host "?????????????????????????????????????????????????????????????????" -ForegroundColor Green
Write-Host ""

cd ..
