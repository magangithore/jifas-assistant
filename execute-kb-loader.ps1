#!/usr/bin/env pwsh

# ???????????????????????????????????????????????????????????????????????????
#  JIFAS Knowledge Base Loader - Full Automated Execution
#  Author: Copilot
#  Status: Production Ready
# ???????????????????????????????????????????????????????????????????????????

param(
    [switch]$NoConfirm = $false
)

$ErrorActionPreference = "Stop"

Write-Host ""
Write-Host "?????????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "?                                                                   ?" -ForegroundColor Cyan
Write-Host "?     JIFAS Knowledge Base Loader - Full Automated Execution        ?" -ForegroundColor Cyan
Write-Host "?                                                                   ?" -ForegroundColor Cyan
Write-Host "?     Status: ? Production Ready                                  ?" -ForegroundColor Cyan
Write-Host "?     Version: 2.0                                                 ?" -ForegroundColor Cyan
Write-Host "?     Expected: 52 files ? 456+ chunks ? SQL Server                ?" -ForegroundColor Cyan
Write-Host "?                                                                   ?" -ForegroundColor Cyan
Write-Host "?????????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

# ???????????????????????????????????????????????????????????????????????????
# STEP 1: Verify Prerequisites
# ???????????????????????????????????????????????????????????????????????????

Write-Host "STEP 1: Verifying Prerequisites..." -ForegroundColor Yellow
Write-Host "???????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host ""

# Check 1: Project structure
Write-Host "[1/4] Checking project structure..." -ForegroundColor Cyan
$requiredDirs = @(
    "KBLoader",
    "Jifas.Assistant",
    "Jifas.Assistant/KnowledgeBase",
    "jifas_assistant.DAL"
)

foreach ($dir in $requiredDirs) {
    if (Test-Path $dir) {
        Write-Host "  ? Found: $dir" -ForegroundColor Green
    } else {
        Write-Host "  ? Missing: $dir" -ForegroundColor Red
        exit 1
    }
}

Write-Host ""

# Check 2: .NET 10 SDK
Write-Host "[2/4] Checking .NET SDK..." -ForegroundColor Cyan
try {
    $dotnetVersion = dotnet --version
    Write-Host "  ? .NET $dotnetVersion installed" -ForegroundColor Green
    if ($dotnetVersion.StartsWith("10.")) {
        Write-Host "  ? .NET 10 detected" -ForegroundColor Green
    }
} catch {
    Write-Host "  ? .NET SDK not found" -ForegroundColor Red
    exit 1
}

Write-Host ""

# Check 3: SQL Server connectivity
Write-Host "[3/4] Checking SQL Server connection..." -ForegroundColor Cyan
$connectionString = "Server=(localdb)\MSSQLLocalDB;Database=JIFAS_Assistant;Integrated Security=true;Encrypt=false;TrustServerCertificate=true"

try {
    $conn = New-Object System.Data.SqlClient.SqlConnection($connectionString)
    $conn.Open()
    Write-Host "  ? SQL Server connected successfully" -ForegroundColor Green
    Write-Host "  ? Database: JIFAS_Assistant" -ForegroundColor Green
    
    # Check table exists
    $cmd = $conn.CreateCommand()
    $cmd.CommandText = "SELECT TABLE_NAME FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_NAME='KnowledgeBaseDocuments'"
    $result = $cmd.ExecuteReader()
    if ($result.Read()) {
        Write-Host "  ? Table KnowledgeBaseDocuments exists" -ForegroundColor Green
    } else {
        Write-Host "  ? Table KnowledgeBaseDocuments not found - will be created" -ForegroundColor Yellow
    }
    $conn.Close()
} catch {
    Write-Host "  ? SQL Server connection failed: $_" -ForegroundColor Red
    Write-Host "  Troubleshooting:" -ForegroundColor Yellow
    Write-Host "    1. Ensure SQL Server is running" -ForegroundColor Gray
    Write-Host "    2. Verify database JIFAS_Assistant exists" -ForegroundColor Gray
    Write-Host "    3. Check connection string in appsettings.json" -ForegroundColor Gray
    exit 1
}

Write-Host ""

# Check 4: Ollama connectivity
Write-Host "[4/4] Checking Ollama server..." -ForegroundColor Cyan
try {
    $response = Invoke-WebRequest -Uri "http://10.0.12.54:11434/api/tags" -TimeoutSec 5 -ErrorAction Stop
    Write-Host "  ? Ollama server responding" -ForegroundColor Green
    
    # Check model
    $tags = $response.Content | ConvertFrom-Json
    if ($tags.models -and ($tags.models.name -contains "qwen3-embedding:4b")) {
        Write-Host "  ? Model qwen3-embedding:4b available" -ForegroundColor Green
    } else {
        Write-Host "  ? Model qwen3-embedding:4b not found - pulling..." -ForegroundColor Yellow
        Write-Host "    Run: ollama pull qwen3-embedding:4b" -ForegroundColor Gray
    }
} catch {
    Write-Host "  ? Ollama server not responding at http://10.0.12.54:11434" -ForegroundColor Red
    Write-Host "  Troubleshooting:" -ForegroundColor Yellow
    Write-Host "    1. Start Ollama: ollama serve" -ForegroundColor Gray
    Write-Host "    2. Check endpoint: curl http://10.0.12.54:11434/api/tags" -ForegroundColor Gray
    Write-Host "    3. Pull model: ollama pull qwen3-embedding:4b" -ForegroundColor Gray
    exit 1
}

Write-Host ""
Write-Host "? All prerequisites verified!" -ForegroundColor Green
Write-Host ""

# ???????????????????????????????????????????????????????????????????????????
# STEP 2: Build Project
# ???????????????????????????????????????????????????????????????????????????

Write-Host "STEP 2: Building KBLoader project..." -ForegroundColor Yellow
Write-Host "???????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host ""

try {
    Push-Location KBLoader
    Write-Host "Building KBLoader..." -ForegroundColor Cyan
    
    $buildOutput = dotnet build --configuration Release 2>&1
    
    if ($LASTEXITCODE -eq 0) {
        Write-Host "? Build successful" -ForegroundColor Green
    } else {
        Write-Host "? Build failed" -ForegroundColor Red
        Write-Host $buildOutput
        exit 1
    }
    
    Pop-Location
} catch {
    Write-Host "? Build error: $_" -ForegroundColor Red
    exit 1
}

Write-Host ""

# ???????????????????????????????????????????????????????????????????????????
# STEP 3: User Confirmation
# ???????????????????????????????????????????????????????????????????????????

if (-not $NoConfirm) {
    Write-Host "STEP 3: Ready to load Knowledge Base" -ForegroundColor Yellow
    Write-Host "???????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
    Write-Host ""
    Write-Host "This will:" -ForegroundColor Cyan
    Write-Host "  1. Clear existing KB documents from database" -ForegroundColor Gray
    Write-Host "  2. Scan 52 KB files from KnowledgeBase/" -ForegroundColor Gray
    Write-Host "  3. Split into ~456 chunks (paragraph-based)" -ForegroundColor Gray
    Write-Host "  4. Generate embeddings via Ollama (1024-dimensional)" -ForegroundColor Gray
    Write-Host "  5. Insert all chunks into SQL Server" -ForegroundColor Gray
    Write-Host ""
    Write-Host "Expected time: ~23 minutes" -ForegroundColor Yellow
    Write-Host ""
    
    $confirm = Read-Host "Continue with KB loading? (Y/N)"
    
    if ($confirm -ne "Y" -and $confirm -ne "y") {
        Write-Host "Operation cancelled by user" -ForegroundColor Yellow
        exit 0
    }
}

Write-Host ""

# ???????????????????????????????????????????????????????????????????????????
# STEP 4: Run KB Loader
# ???????????????????????????????????????????????????????????????????????????

Write-Host "STEP 4: Running KB Loader..." -ForegroundColor Yellow
Write-Host "???????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host ""

$startTime = Get-Date

try {
    Write-Host "Starting API server..." -ForegroundColor Cyan
    Write-Host "This may take a few seconds..." -ForegroundColor Gray
    
    # Start API in background process
    $apiProcess = Start-Process -FilePath "dotnet" -ArgumentList "run --configuration Release" -WorkingDirectory ".\Jifas.Assistant" -PassThru -NoNewWindow
    
    # Wait for API to start
    Start-Sleep -Seconds 5
    
    # Check if API is running
    try {
        $apiCheck = Invoke-WebRequest -Uri "http://localhost:5000/api/health" -TimeoutSec 5 -ErrorAction SilentlyContinue
        if ($apiCheck.StatusCode -eq 200) {
            Write-Host "? API started successfully" -ForegroundColor Green
        }
    }
    catch {
        Write-Host "? API may be slow to start, will retry..." -ForegroundColor Yellow
        Start-Sleep -Seconds 3
    }
    
    Write-Host ""
    Write-Host "Now running KB Loader..." -ForegroundColor Cyan
    Write-Host ""
    
    Push-Location KBLoader
    
    Write-Host "Executing: dotnet run --configuration Release -- --yes" -ForegroundColor Cyan
    Write-Host ""
    
    dotnet run --configuration Release -- --yes
    
    if ($LASTEXITCODE -ne 0) {
        Write-Host ""
        Write-Host "? KB Loader execution failed" -ForegroundColor Red
    }
    
    Pop-Location
    
    # Stop API process
    if ($apiProcess) {
        Stop-Process -Id $apiProcess.Id -Force -ErrorAction SilentlyContinue
        Write-Host ""
        Write-Host "API server stopped" -ForegroundColor Gray
    }
} catch {
    Write-Host "? Execution error: $_" -ForegroundColor Red
}

$endTime = Get-Date
$duration = $endTime - $startTime

Write-Host ""

# ???????????????????????????????????????????????????????????????????????????
# STEP 5: Verification
# ???????????????????????????????????????????????????????????????????????????

Write-Host "STEP 5: Verifying results..." -ForegroundColor Yellow
Write-Host "???????????????????????????????????????????????????????????????????" -ForegroundColor Yellow
Write-Host ""

try {
    $conn = New-Object System.Data.SqlClient.SqlConnection($connectionString)
    $conn.Open()
    
    $cmd = $conn.CreateCommand()
    $cmd.CommandText = "SELECT COUNT(*) FROM KnowledgeBaseDocuments"
    $totalCount = $cmd.ExecuteScalar()
    
    Write-Host "  ? Total documents in database: $totalCount" -ForegroundColor Green
    
    if ($totalCount -gt 0) {
        # Get breakdown by category
        $cmd.CommandText = "SELECT Category, COUNT(*) as Count FROM KnowledgeBaseDocuments GROUP BY Category ORDER BY Count DESC"
        $reader = $cmd.ExecuteReader()
        
        Write-Host ""
        Write-Host "Breakdown by category:" -ForegroundColor Cyan
        while ($reader.Read()) {
            $category = $reader["Category"]
            $count = $reader["Count"]
            Write-Host "  • $category : $count chunks" -ForegroundColor Gray
        }
        $reader.Close()
    }
    
    $conn.Close()
} catch {
    Write-Host "  ? Could not verify database: $_" -ForegroundColor Yellow
}

Write-Host ""

# ???????????????????????????????????????????????????????????????????????????
# Final Summary
# ???????????????????????????????????????????????????????????????????????????

Write-Host "?????????????????????????????????????????????????????????????????????" -ForegroundColor Green
Write-Host "?                                                                   ?" -ForegroundColor Green
Write-Host "?                  ? KNOWLEDGE BASE LOADED SUCCESSFULLY            ?" -ForegroundColor Green
Write-Host "?                                                                   ?" -ForegroundColor Green
Write-Host "?  Execution Time: $($duration.TotalSeconds) seconds                        " -ForegroundColor Green
Write-Host "?  Total Chunks: $totalCount documents inserted                    " -ForegroundColor Green
Write-Host "?  Status: Ready for RAG queries                                   ?" -ForegroundColor Green
Write-Host "?                                                                   ?" -ForegroundColor Green
Write-Host "?????????????????????????????????????????????????????????????????????" -ForegroundColor Green

Write-Host ""
Write-Host "Next steps:" -ForegroundColor Yellow
Write-Host "  1. Verify in database: SELECT TOP 10 * FROM KnowledgeBaseDocuments" -ForegroundColor Gray
Write-Host "  2. Start API: dotnet run (from Jifas.Assistant/)" -ForegroundColor Gray
Write-Host "  3. Test RAG: Send chat query to API" -ForegroundColor Gray
Write-Host ""

exit 0
